function PowerA234(A) {
    var A2 = Math.pow(A, 2);
    var A3 = Math.pow(A, 3);
    var A4 = Math.pow(A, 4);
    return "Power 2: " + A2 + " Power 3: " + A3 + " Power 4: " + A4;
}
console.log(PowerA234(1));
console.log(PowerA234(2));
console.log(PowerA234(3));
console.log(PowerA234(4));
console.log(PowerA234(5));
